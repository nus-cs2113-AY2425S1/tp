package seedu.budgetbuddy.transaction;

/**
 * Represents the different categories of expenses in the budget management system.
 */
public enum Category {
    /** Represents food-related expenses. */
    FOOD,

    /** Represents transportation-related expenses. */
    TRANSPORT,

    /** Represents entertainment-related expenses. */
    ENTERTAINMENT,

    /** Represents education-related expenses. */
    EDUCATION,

    /** Represents utility-related expenses, such as electricity and water bills. */
    UTILITIES,

    /** Represents miscellaneous expenses that do not fall into other categories. */
    OTHERS,

}
